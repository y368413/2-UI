                    local existingGemLink = GetExistingSocketLink(1)
                    local isUnique, uniqueType = DSH:IsGemUnique(existingGemLink, select(1, existingGemLink))

                    if isUnique then
                        DSHPrint(SHL["UNIQUE_WARNING"])
                    else
                        AcceptSockets()
                    end
                end
            end
		end
		
	end
end


